export const getItemStatText = (item) => {
  const stats = item?.stats || {};
  const parts = [];
  if (stats.hp) parts.push(`체력 ${stats.hp > 0 ? '+' : ''}${stats.hp}`);
  if (stats.attack) parts.push(`공격력 ${stats.attack > 0 ? '+' : ''}${stats.attack}`);
  if (stats.attackSpeed) parts.push(`공격속도 ${stats.attackSpeed > 0 ? '+' : ''}${stats.attackSpeed}`);
  if (stats.speed) parts.push(`스피드 ${stats.speed > 0 ? '+' : ''}${stats.speed}`);
  if (stats.critChance) parts.push(`치명타 ${stats.critChance > 0 ? '+' : ''}${stats.critChance}%`);
  return parts.length ? parts.join(' / ') : '능력치 없음';
};
